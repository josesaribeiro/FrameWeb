package br.ufes.inf.nemo.frameweb.codegenerator.engine;

public class NullUtils {
	
	/**
	 * 
	 * @param object
	 * @return
	 */
	public boolean isNull(Object object) {
		return object == null;
	}
	
	/**
	 * 
	 * @param object
	 * @return
	 */
	public boolean isNotNull(Object object) {
		return object != null;
	}
	
}
